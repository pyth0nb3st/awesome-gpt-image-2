const ACRONYMS = new Map([
  ["ai", "AI"],
  ["api", "API"],
  ["qa", "QA"],
  ["ui", "UI"],
  ["ux", "UX"],
]);

const titleCaseTag = (tag) =>
  String(tag)
    .split("-")
    .filter(Boolean)
    .map((part) => ACRONYMS.get(part) ?? part.charAt(0).toUpperCase() + part.slice(1))
    .join(" ");

export const tagLabel = (tag, locale = DEFAULT_LOCALE) =>
  TAG_LABELS[normalizeLocale(locale)]?.[tag] ?? TAG_LABELS[DEFAULT_LOCALE]?.[tag] ?? titleCaseTag(tag);
