export const getCopy = (locale = DEFAULT_LOCALE) => copy[locale] ?? copy[DEFAULT_LOCALE];

export const normalizeLocale = (locale) => (LOCALES.includes(locale) ? locale : DEFAULT_LOCALE);

export const localizedPath = (path = "/", locale = DEFAULT_LOCALE) => {
  const cleanPath = `/${String(path).replace(/^\/+/, "")}`;
  if (locale === DEFAULT_LOCALE) return cleanPath;
  return cleanPath === "/" ? `/${locale}/` : `/${locale}${cleanPath}`;
};

